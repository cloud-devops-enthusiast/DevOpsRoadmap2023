package Array;

public class traverseArray {
    public static void main(String[] args){
        int arr[] = {95,61,62,24,49};
        int i;
        System.out.println("Elements of array are:");
        for(i=0;i<arr.length;i++){
            System.out.println(arr[i]+"[Index:"+i+"]");
        }
    }
}
/*
Output:
Elements of array are:
95[Index:0]
61[Index:1]
62[Index:2]
24[Index:3]
49[Index:4]
*/