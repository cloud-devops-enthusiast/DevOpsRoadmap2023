public class ifelseStatement {
    public static void main(String[] args){
        int number;
        number=23;
        if (number%2==0){
            System.out.println(number+" is an even number");
        }
        else{
            System.out.println(number+" is an odd number");
        }
    }
}
/*
Output:
23 is an odd number
 */